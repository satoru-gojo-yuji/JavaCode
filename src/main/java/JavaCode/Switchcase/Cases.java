package JavaCode.Switchcase;

public class Cases {

    public static void main(String[] args) {
        int i = 6;

        switch (i) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
            default:
                System.out.println("to the default ");

        }
    }
}
